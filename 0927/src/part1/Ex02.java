//자동완성 : option + space
//한줄코드삭제 : comd + d
public class Ex02 {
	public static void main(String[] args) {
		System.out.println(100+200);//덧
		System.out.println(100.2);
		System.out.println('A');//문자,charactor
		System.out.println("asdf"); //문자열, string
		System.out.println("asdf"+100); //문자열, string, 연결 
		System.out.println(""+10+20); //"" empty string 연결
		
		System.out.println(25);
		System.out.println("25");
		//25를 출력하되 + or "" 이용해서 다양하게 출
		
		System.out.println("" + 2 + 5);
		System.out.println( 10 + 15 + "");
		System.out.println("" + 25);
		System.out.println(2 +""+ 5);
	}

}

